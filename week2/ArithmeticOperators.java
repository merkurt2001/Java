package a_review.week2;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		//Arithmetic Operator
		//+ , - , * , / , %
		System.out.println( 9 + 5  );
		System.out.println( 9 - 5  );
		System.out.println( 9 * 5  );
		
		// division between whole numbers always result in whole number (int)
		System.out.println( 9 / 5  );
		// modulus or remainder operator will return the remaining part after division
		System.out.println( 9 % 5  );
		
		// 10 apples divide by 3 friends , 
		// each will get 3 apples 
		// and one apple will remain 
		System.out.println( 10 / 3);  // 3
		System.out.println( 10 % 3 ); // 1 
		
		
		// a whole number by itself without assigning to any variable 
		// for example 10  , it will be always assumed as int for compiler 
		
		// a fractional number by itself without assigning to any variable 
		// for example 10.99  , it will be always assumed as double for compiler 
		
		
		
		

	}

}
