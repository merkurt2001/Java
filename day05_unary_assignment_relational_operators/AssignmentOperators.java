package day05_unary_assignment_relational_operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		

			int i = 100;
			System.out.println(i);
			
			i+=1;
			System.out.println(i);
			
			i-=2;
			System.out.println(i);
			
			i*=3;
			System.out.println(i);
			
			i/=4;
			System.out.println(i);
			
			i%=5;
			System.out.println(i);

	}

}
