package day35_encapsulation;

public class Person {
	
	private String name;
	private int age; 
	private char gender;
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}

	public char getGender() {
		return gender;
	}
	public void setGender(char gender) {
		this.gender = gender;
	}


	
	

	
	
	
	
	

	

	

}
