package day13_methods_Part1;

public class task56_convertKM {

	public static void main(String[] args) {
		
		
		convertKM();
		

	}
	
	public static void convertKM() {
		
		int mile=80;
		double km=mile * 1.60934;
		
		System.out.println(mile + " mile = " + km + " km");
		
	}

}
